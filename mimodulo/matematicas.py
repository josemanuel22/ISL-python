def suma(a, b):
    """
    Función para sumar dos números.

    :param a: valor a la izquierda de la suma.
    :type a: int.
    :param b: valor a la derecha de la suma.
    :type b: int.
    :return:  el resultado.
    :rtype: int

    >>> suma(1, 2)
    3
    """
    return a + b


def resta(a, b):
    """
    Función para restar dos números.

    :param a: valor a la izquierda de la resta.
    :type a: int.
    :param b: valor a la derecha de la resta.
    :type b: int.
    :return:  el resultado.
    :rtype: int

    >>> resta(1, 2)
    -1
    """

    return a - b

